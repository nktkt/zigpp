const testing = std.testing;

fn tmpAbsPath(allocator: std.mem.Allocator, tmp: *std.testing.TmpDir) ![]u8 {
    var buf: [std.fs.max_path_bytes]u8 = undefined;
    const real = try tmp.dir.realpath(".", &buf);
    return allocator.dupe(u8, real);
}

test "scaffold: fresh dir creates exactly 4 non-empty files with substituted name" {
    var tmp = std.testing.tmpDir(.{ .iterate = true });
    defer tmp.cleanup();

    const allocator = testing.allocator;

    const tmp_path = try tmpAbsPath(allocator, &tmp);
    defer allocator.free(tmp_path);

    const proj_path = try std.fs.path.join(allocator, &.{ tmp_path, "demo" });
    defer allocator.free(proj_path);

    try scaffold(allocator, proj_path, "demo", false);

    // Open from the tmp side for inspection.
    var proj = try tmp.dir.openDir("demo", .{ .iterate = true });
    defer proj.close();

    var count: usize = 0;
    var seen_main = false;
    var seen_build = false;
    var seen_zon = false;
    var seen_gitignore = false;
    var it = proj.iterate();
    while (try it.next()) |entry| {
        count += 1;
        if (std.mem.eql(u8, entry.name, "main.zpp")) seen_main = true;
        if (std.mem.eql(u8, entry.name, "build.zig")) seen_build = true;
        if (std.mem.eql(u8, entry.name, "build.zig.zon")) seen_zon = true;
        if (std.mem.eql(u8, entry.name, ".gitignore")) seen_gitignore = true;
    }
    try testing.expectEqual(@as(usize, 4), count);
    try testing.expect(seen_main);
    try testing.expect(seen_build);
    try testing.expect(seen_zon);
    try testing.expect(seen_gitignore);

    const main_src = try proj.readFileAlloc(allocator, "main.zpp", 1 << 20);
    defer allocator.free(main_src);
    try testing.expect(main_src.len > 0);
    // The substituted greeting line must show the project name.
    try testing.expect(std.mem.indexOf(u8, main_src, "Hello{ .name = \"demo\" }") != null);

    const build_src = try proj.readFileAlloc(allocator, "build.zig", 1 << 20);
    defer allocator.free(build_src);
    try testing.expect(build_src.len > 0);

    const zon_src = try proj.readFileAlloc(allocator, "build.zig.zon", 1 << 20);
    defer allocator.free(zon_src);
    try testing.expect(zon_src.len > 0);

    const gi_src = try proj.readFileAlloc(allocator, ".gitignore", 1 << 20);
    defer allocator.free(gi_src);
    try testing.expect(gi_src.len > 0);
}

test "scaffold: non-empty dir without force returns DirNotEmpty" {
    var tmp = std.testing.tmpDir(.{ .iterate = true });
    defer tmp.cleanup();

    try tmp.dir.makePath("proj");
    try tmp.dir.writeFile(.{ .sub_path = "proj/squatter.txt", .data = "x\n" });

    const allocator = testing.allocator;

    const tmp_path = try tmpAbsPath(allocator, &tmp);
    defer allocator.free(tmp_path);

    const proj_path = try std.fs.path.join(allocator, &.{ tmp_path, "proj" });
    defer allocator.free(proj_path);

    const result = scaffold(allocator, proj_path, "proj", false);
    try testing.expectError(ScaffoldError.DirNotEmpty, result);
}

test "scaffold: --force overwrites template files in non-empty dir" {
    var tmp = std.testing.tmpDir(.{ .iterate = true });
    defer tmp.cleanup();

    try tmp.dir.makePath("proj");
    // Pre-existing junk that --force must NOT delete.
    try tmp.dir.writeFile(.{ .sub_path = "proj/squatter.txt", .data = "x\n" });
    // Pre-existing main.zpp with stale content.
    try tmp.dir.writeFile(.{ .sub_path = "proj/main.zpp", .data = "// stale\n" });

    const allocator = testing.allocator;

    const tmp_path = try tmpAbsPath(allocator, &tmp);
    defer allocator.free(tmp_path);

    const proj_path = try std.fs.path.join(allocator, &.{ tmp_path, "proj" });
    defer allocator.free(proj_path);

    try scaffold(allocator, proj_path, "proj", true);

    var proj = try tmp.dir.openDir("proj", .{ .iterate = true });
    defer proj.close();

    // Stale junk preserved.
    const sq = try proj.readFileAlloc(allocator, "squatter.txt", 1 << 20);
    defer allocator.free(sq);
    try testing.expectEqualStrings("x\n", sq);

    // Template overwrite happened.
    const main_src = try proj.readFileAlloc(allocator, "main.zpp", 1 << 20);
    defer allocator.free(main_src);
    try testing.expect(std.mem.indexOf(u8, main_src, "Hello{ .name = \"proj\" }") != null);
    try testing.expect(std.mem.indexOf(u8, main_src, "// stale") == null);
}

test "scaffold: name with dash sanitizes for zon but stays raw in build.zig" {
    var tmp = std.testing.tmpDir(.{ .iterate = true });
    defer tmp.cleanup();

    const allocator = testing.allocator;

    const tmp_path = try tmpAbsPath(allocator, &tmp);
    defer allocator.free(tmp_path);

    const proj_path = try std.fs.path.join(allocator, &.{ tmp_path, "my-app" });
    defer allocator.free(proj_path);

    try scaffold(allocator, proj_path, "my-app", false);

    var proj = try tmp.dir.openDir("my-app", .{ .iterate = true });
    defer proj.close();

    const zon_src = try proj.readFileAlloc(allocator, "build.zig.zon", 1 << 20);
    defer allocator.free(zon_src);
    try testing.expect(std.mem.indexOf(u8, zon_src, ".name = .my_app,") != null);

    const build_src = try proj.readFileAlloc(allocator, "build.zig", 1 << 20);
    defer allocator.free(build_src);
    // The Zig string literal in addExecutable's .name = "..." can hold a dash.
    try testing.expect(std.mem.indexOf(u8, build_src, "\"my-app\"") != null);
}

test "computeFingerprint: stable across repeated calls" {
    const a = computeFingerprint("demo");
    const b = computeFingerprint("demo");
    try testing.expectEqual(a, b);

    // Different names should (almost certainly) hash differently.
    const c = computeFingerprint("other");
    try testing.expect(a != c);
}

test "sanitizeProjectName: leading digit gets underscore prefix; non-ident chars become underscore" {
    const allocator = testing.allocator;

    const a = try sanitizeProjectName(allocator, "9lives");
    defer allocator.free(a);
    try testing.expectEqualStrings("_9lives", a);

    const b = try sanitizeProjectName(allocator, "my.cool-pkg");
    defer allocator.free(b);
    try testing.expectEqualStrings("my_cool_pkg", b);

    const c = try sanitizeProjectName(allocator, "");
    defer allocator.free(c);
    try testing.expectEqualStrings("world", c);
}
